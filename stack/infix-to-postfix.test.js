import infixToPostfix from "./infix-to-postfix"

test("correct result for all test cases", () => {
  expect(infixToPostfix(["12", "+", "3"])).toEqual(["12", "3", "+"])
  expect(
    infixToPostfix([
      "(",
      "1",
      "+",
      "(",
      "4",
      "+",
      "5",
      "+",
      "3",
      ")",
      "-",
      "3",
      ")",
      "+",
      "(",
      "9",
      "+",
      "8",
      ")",
    ])
  ).toEqual(["1", "4", "5", "+", "3", "+", "+", "3", "-", "9", "8", "+", "+"])
  expect(
    infixToPostfix([
      "(",
      "1",
      "+",
      "(",
      "4",
      "+",
      "5",
      "+",
      "3",
      ")",
      "/",
      "4",
      "-",
      "3",
      ")",
      "+",
      "(",
      "6",
      "+",
      "8",
      ")",
      "*",
      "3",
    ])
  ).toEqual(["1", "4", "5", "+", "3", "+", "4", "/", "+", "3", "-", "6", "8", "+", "3", "*", "+"])
})
